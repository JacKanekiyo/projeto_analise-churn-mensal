# calcula a diferença em dias e transforma em meses aproximados
base_final['Dias_Contrato'] = (base_final['Data_Cancelamento_DT'] - base_final['Data_Ativacao_DT']).dt.days
base_final['Meses_Contrato'] = (base_final['Dias_Contrato'] / 30.44).fillna(0).astype(int)

# categorizando o tempo de casa para o gráfico ficar executivo
def faixas_tempo(meses):
    if meses <= 3: return '0 a 3 meses (Crítico)'
    elif meses <= 6: return '4 a 6 meses'
    elif meses <= 12: return '7 a 12 meses (1 Ano)'
    elif meses <= 24: return '1 a 2 anos'
    else: return 'Mais de 2 anos (Veterano)'

base_final['Tempo_de_Casa'] = base_final['Meses_Contrato'].apply(faixas_tempo)
ordem_tempo = ['0 a 3 meses (Crítico)', '4 a 6 meses', '7 a 12 meses (1 Ano)', '1 a 2 anos', 'Mais de 2 anos (Veterano)']

