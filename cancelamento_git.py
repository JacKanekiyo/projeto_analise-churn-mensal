"""# **CARREGAMENTO DOS ARQUIVOS**"""

# se for rodar localmente fora do Colab, você pode comentar a linha do drive.mount
drive.mount('/content/drive')

# definindo os caminhos dos arquivos Drive
path_pasta = './dados_ficticios/'

file_cancelamentos = path_pasta + 'cancelamentos_abril_fake.xlsx'
file_chamados = path_pasta + 'chamados_os_abril_fake.csv'

#lendo o CSV de chamados e definindo como os_historico para manter a consistência do script
chamados = pd.read_csv(file_chamados, sep=';', encoding='utf-8', engine='python')
os_historico = chamados

cancelamentos = pd.read_excel(file_cancelamentos)

print("--- ARQUIVOS CARREGADOS COM SUCESSO ---")
print(f"Total de Cancelados em Abril: {len(cancelamentos)}")
print(f"Total de O.S. no Histórico: {len(os_historico)}")

"""**TRATAMENTO DA BASE DE CANCELAMENTOS (ABRIL)**"""

# extrair o ID numérico do início da string
cancelamentos['ID_Sistema'] = cancelamentos['Cliente'].str.extract(r'^(\d+)').astype('Int64')

# separar o nome do cliente limpando o ID e o traço da frente
cancelamentos['Nome_Cliente'] = cancelamentos['Cliente'].str.replace(r'^\d+\s*-\s*', '', regex=True)

# trata o valor do plano: substitui a vírgula por ponto e converte para float
cancelamentos['Valor_Plano_Num'] = cancelamentos['Valor do Plano'].astype(str).str.replace(',', '.')
cancelamentos['Valor_Plano_Num'] = pd.to_numeric(cancelamentos['Valor_Plano_Num'], errors='coerce')

# ajustar as colunas de data que não estavam formatadas
cancelamentos['Data_Cancelamento_DT'] = pd.to_datetime(cancelamentos['Data Cancelamento'], format='%d/%m/%Y', errors='coerce')
cancelamentos['Data_Ativacao_DT'] = pd.to_datetime(cancelamentos['Data Ativação'], format='%d/%m/%Y', errors='coerce')

"""**TRATAMENTO E FILTRAGEM DO HISTÓRICO DE O.S. (JAN A ABR)**"""

# garantindo que a coluna ID - Cliente seja lida como numérico Int64 para o cruzamento
os_historico['ID_Cliente_Clean'] = pd.to_numeric(os_historico['ID - Cliente'], errors='coerce').astype('Int64')

# normalizar os textos dos assuntos para maiúsculas
os_historico['Assunto_Up'] = os_historico['Assunto'].astype(str).str.upper().str.strip()

# LISTA DE EXCLUSÃO: filtrar os procedimentos de rotina para isolar defeitos reais
os_ignoradas = [
    'RETIRADA DE EQUIPAMENTO', 'RETIRADA DE EQUIPAMENTOS',
    'ATIVAÇÃO INICIAL FIBRA ÓPTICA', 'VERIFICAR FIBRA - ATIVAÇÃO INICIAL',
    'ATIVAÇÃO INICIAL URBANA', 'TRANSFERÊNCIA DE ENDEREÇO',
    'PASSAGEM DE CABO DE REDE', 'TRANSFERÊNCIA DE ENDEREÇO - FIBRA'
]
os_ignoradas_norm = [item.upper() for item in os_ignoradas]

# contar estritamente as O.S. que indicam falhas técnicas ou manutenções
falhas_historicas = os_historico[~os_historico['Assunto_Up'].isin(os_ignoradas_norm)].copy()

"""**CRIAÇÃO DAS MÉTRICAS TÉCNICAS REAIS POR CLIENTE**"""

# Métrica A: total de ordens de serviço (visitas ao todo, incluindo rotinas) que o cliente gerou no ano
df_total_visitas = os_historico.groupby('ID_Cliente_Clean').size().reset_index(name='Total_Visitas_Tecnicas')

# Métrica B: quantidade de chamados estritamente de falhas/defeitos
df_qtd_falhas = falhas_historicas.groupby('ID_Cliente_Clean').size().reset_index(name='Qtd_Chamados_Defeito')

# Métrica C: qual foi a falha técnica principal (mais frequente) no histórico do cliente
df_falha_recorrente = (falhas_historicas.groupby('ID_Cliente_Clean')['Assunto']
                       .agg(lambda x: x.value_counts().index[0] if not x.empty else 'Sem falhas no histórico')
                       .reset_index(name='Historico_Defeito_Principal'))

"""**MASTER JOIN (União Estruturada)**"""

# unir tabelas de cancelados com as agregações de O.S. históricas baseadas no ID do Sistema
base_final = pd.merge(cancelamentos, df_total_visitas.rename(columns={'ID_Cliente_Clean': 'ID_Sistema'}), on='ID_Sistema', how='left')
base_final = pd.merge(base_final, df_qtd_falhas.rename(columns={'ID_Cliente_Clean': 'ID_Sistema'}), on='ID_Sistema', how='left')
base_final = pd.merge(base_final, df_falha_recorrente.rename(columns={'ID_Cliente_Clean': 'ID_Sistema'}), on='ID_Sistema', how='left')

# tratamento pós-join para preencher valores vazios (NaN) de quem nunca teve O.S.
base_final['Total_Visitas_Tecnicas'] = base_final['Total_Visitas_Tecnicas'].fillna(0).astype(int)
base_final['Qtd_Chamados_Defeito'] = base_final['Qtd_Chamados_Defeito'].fillna(0).astype(int)
base_final['Historico_Defeito_Principal'] = base_final['Historico_Defeito_Principal'].fillna('Nenhuma O.S. no ano')

# ordenar clientes que mais sofreram com defeitos
base_final = base_final.sort_values(by='Qtd_Chamados_Defeito', ascending=False).reset_index(drop=True)

"""**EXPORTAÇÃO E VALIDAÇÃO DOS DADOS**"""

# seleção de colunas para validação em tela
colunas_executivas = [
    'ID_Sistema', 'Nome_Cliente', 'Motivo', 'Cidade',
    'Total_Visitas_Tecnicas', 'Qtd_Chamados_Defeito', 'Historico_Defeito_Principal'
]

print("\n--- TOP 10 HISTÓRICO TÉCNICO ---")
print(base_final[colunas_executivas].head(10))

#salvar a base mestra em formato Excel
caminho_saida = './base_master_cancelamentos_abril.xlsx'
base_final.to_excel(caminho_saida, index=False)
print("\nBase de Abril gerada localmente com sucesso.")

